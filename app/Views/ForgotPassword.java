    private boolean validatePhoneNumber() {
        String phoneNumber = mPhoneNumberField.getText().toString();
        if (TextUtils.isEmpty(phoneNumber)) {
            mPhoneNumberField.setError("Invalid phone number.");
            return false;
        }

        return true;
    }

    private void enableViews(View... views) {
        for (View v : views) {
            v.setEnabled(true);
        }
    }

    private void disableViews(View... views) {
        for (View v : views) {
            v.setEnabled(false);
        }
    }

    @Override
    public void onClick(View view) {
        switch (view.getId()) {
            case R.id.buttonStartVerification:
                if (!validatePhoneNumber()) {
                    return;
                }
                phone ="+91" + mPhoneNumberField.getText().toString();
//                startPhoneNumberVerification(phone);
                new checkIfUserExist(phone).execute();
                break;
            case R.id.buttonVerifyPhone:
                String code = mVerificationField.getText().toString();
                if (TextUtils.isEmpty(code)) {
                    mVerificationField.setError("Cannot be empty.");
                    return;
                }
                verifyPhoneNumberWithCode(mVerificationId, code);
                break;
            case R.id.buttonResend:
                resendVerificationCode(mPhoneNumberField.getText().toString(), mResendToken);
                break;
            case R.id.signOutButton:
                     signOut();
                break;
        }
    }
    private void signOut() {
        mAuth.signOut();
        updateUI(STATE_INITIALIZED);
    }

    @SuppressLint("StaticFieldLeak")
    class checkIfUserExist extends AsyncTask<String, String, String> {
        boolean success = false;
        HashMap<String, String> params = new HashMap<>();
        ProgressDialog progress;
        private String phone;

        @Override
        protected void onPreExecute() {
            super.onPreExecute();
            progress = new ProgressDialog(ForgotPassword.this);
            progress.setMessage("Checking User..");
            progress.setIndeterminate(true);
            progress.setProgress(0);
            progress.show();
            params.put("phone", phone);
        }

        checkIfUserExist(String phone) {
            this.phone = phone;
        }


        @Override
        protected void onPostExecute(String s) {
            super.onPostExecute(s);
            progress.dismiss();
            JSONObject jsonObject = null;
            try {
                jsonObject = new JSONObject(s);

                Log.e("result", s);
                if (jsonObject.has("success")) {
                    if (jsonObject.getJSONArray("data").length()>0) {
                        startPhoneNumberVerification(phone);
                    } else {
                        Toast.makeText(ForgotPassword.this, "Phone no does not exist, Please register first !!", Toast.LENGTH_SHORT).show();

                    }
                }
            } catch (JSONException e) {
                e.printStackTrace();
            }


        }

        @Override
        protected String doInBackground(String... strings) {
            String result = "";
            try {
                Gson gson = new Gson();
                String json = gson.toJson(params);
                result = Server.post(getResources().getString(R.string.checkIfUserExist), json);
                success = true;
            } catch (Exception e) {
                e.printStackTrace();
            }

            System.out.println("Result:" + result);
            return result;
        }
    }

